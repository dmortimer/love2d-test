-- instantiates the map by parsing the tilemap and referencing the spritesheet
function map:create()
    map.mapWidthPixels = map.mapWidth * map.tileWidth
    map.mapHeightPixels = map.mapHeight * map.tileHeight

    map.spritesheet:setFilter("nearest", "linear")
    map.tileSprites = generateQuads(map.spritesheet, map.tileWidth, map.tileHeight)

    map.tilesetBatch = love.graphics.newSpriteBatch(map.spritesheet, map.mapWidth * map.mapHeight)

    -- creates spritesheet of map to be drawn
    for i,row in pairs(map.tiles) do
        for j,tile in pairs(row) do
            -- add 1 index for tileSprites, remove one from tile coordinates
            map.tilesetBatch:add(map.tileSprites[tile + 1], (j - 1) * map.tileHeight, (i - 1) * map.tileWidth)
        end
    end
end


-- draws the spritesheet to the map
function map:draw()
    love.graphics.draw(map.tilesetBatch)
end

